const mongoose = require('mongoose');

const wasteSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    quantity: {
        type: Number,
        required: true
    },
    createdAt: {
        type: Date,
        default: Date.now
    }
});

const Waste = mongoose.model('Waste', wasteSchema);

module.exports = Waste;
